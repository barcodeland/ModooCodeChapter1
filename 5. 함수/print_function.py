# 나는 '이제'라는 말을 앞에 항상 붙이는 버릇이 있어. 

# 그러면 할 말에다 항상 붙여주면 좋겠다. 
halmal1 = "코딩이 정말 하고 싶었죠?"

print("이제 " + halmal1)

# 그래 이렇게 계속 똑같은 짓을 반복하면 내 삶이 윤택해지겠네
halmal2 = "진짜로 할 수 있어요."

print("이제 " + halmal2)

# 인내심이 생겼다. 

halmal3 = "진짜 못참겠다. "

print("이제 " + halmal3)



